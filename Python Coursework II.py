import seaborn as sns
sns.set_style('whitegrid')
penguins = sns.load_dataset('penguins')
penguins.head()

sns.barplot(x='species', y='flipper_length_mm', data=penguins,  hue='species')
plt.show()

sns.violinplot(x='species', y='flipper_length_mm', data=penguins, hue='species')
plt.show()

sns.pairplot(penguins, hue='species')
plt.show()

numerical_cols = penguins.select_dtypes(include=['number']).columns
correlation_matrix = penguins[numerical_cols].corr()

sns.heatmap(correlation_matrix, annot=True)
plt.show()

sns.scatterplot(data=penguins, x='bill_length_mm', y='bill_depth_mm', hue='species')
plt.show()

